        <h1>REGULAMENTO OFICIAL — UGC 4ARTISTS</h1>
<h2>1. Objetivo</h2>
<p>O presente regulamento estabelece as condições de participação no movimento UGC 4ARTISTS ou UGC PARA MÚSICOS, que conecta artistas, criadores de conteúdo e gravadoras interessadas em campanhas de divulgação musical remuneradas.</p>
<h2>2. Participação</h2>
<ol>
<li>Podem participar artistas, músicos, cantores, instrumentistas e criadores de conteúdo maiores de 18 anos ou menores assistidos por responsável legal.</li>
<li>A inscrição é gratuita e realizada exclusivamente pelo formulário oficial disponível no link da bio do perfil @ugc4artists.</li>
<li>A equipe da UGC 4 ARTISTS reserva-se o direito de selecionar participantes com base em critérios de engajamento, adequação de conteúdo e disponibilidade.</li>
</ol>
<h2>3. Seleção e Convites para Campanhas</h2>
<ol>
<li>Após a inscrição, os participantes elegíveis poderão ser convidados para campanhas específicas de divulgação de músicas.</li>
<li>Cada campanha possuirá briefing, prazos e valores definidos individualmente.</li>
<li>O participante que aceitar uma campanha compromete-se a cumprir o cronograma e as diretrizes de conteúdo.</li>
</ol>
<h2>4. Remuneração</h2>
<ol>
<li>O valor da remuneração será informado antes da confirmação da participação em cada campanha.</li>
<li>O pagamento ocorrerá mediante comprovação da entrega do conteúdo conforme as instruções do briefing.</li>
<li>Os valores poderão variar conforme engajamento, formato e complexidade do conteúdo.</li>
</ol>
<h2>5. Exclusividade e Sigilo</h2>
<ol>
<li>O participante autoriza o uso de imagem, voz e conteúdo produzido nas campanhas, exclusivamente para fins de divulgação da música e das ações da UGC 4ARTISTS.</li>
<li>É vedado ao participante compartilhar briefings, valores, campanhas ou informações internas sem autorização expressa.</li>
<li>A participação implica exclusividade de 24 meses dentro do movimento UGC 4ARTISTS para campanhas do mesmo formato.</li>
</ol>
<h2>6. Conduta e Desclassificação</h2>
<ol>
<li>Serão desclassificados os participantes que não cumprirem prazos, entregarem materiais fora do padrão acordado ou desrespeitarem regras de convivência com outros artistas.</li>
<li>A UGC 4ARTISTS poderá suspender ou excluir participantes em caso de conduta antiética, uso indevido da marca ou descumprimento contratual.</li>
</ol>
<h2>7. Direitos e Obrigações da UGC 4ARTISTS</h2>
<ol>
<li>A plataforma compromete-se a manter confidencialidade dos dados dos inscritos.</li>
<li>A inscrição não garante participação imediata em campanhas, mas inclusão no banco oficial de talentos.</li>
<li>O movimento poderá alterar este regulamento mediante aviso prévio nas redes oficiais.</li>
</ol>
<h2>8. Vigência e Foro</h2>
<p>Este regulamento é válido a partir da data de publicação.
Eventuais divergências serão resolvidas no foro da cidade do Rio de Janeiro/RJ.</p>
